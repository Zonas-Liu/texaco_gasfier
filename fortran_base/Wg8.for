      DIMENSION DHT(15)

C***  DHDT IS THE TEMPERATURE DIFFERENCE OF ENTHALPHY OF GAS
C	GAS NUMBER: 1-O2,2-CH4,3-CO,4-CO2,5-H2S,6-H2,7-N2,8-H2O
C				9-CARBON,10-ASH

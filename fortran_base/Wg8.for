C *** FEEDH(2) STANDS FOR THE ENTHALPHY OF NITROGEN OF AIR IN I CELL
C *** FEEDH(3) STANDS FOR THE ENTHALPHY OF NITROGEN OF AIR IN I CELL

C *** FEDPH(1) STANDS FOR THE ENTHALPHY OF CARBON PARTICLE IN I CELL  
C *** FEDPH(2) STANDS FOR THE ENTHALPHY OF ASH IN I CELL  


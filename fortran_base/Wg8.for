      DHDT(J,I)=DHT(J)-HENTH(J,I)
 11   CONTINUE
	J=8
	IF(T(I).GT.373.15) THEN
      DHT(J)=ENTHP(J,'G   ',T(I)+1.,PWK)
      HENTH(J,I)=ENTHP(J,'G   ',T(I),PWK)
      DHDT(J,I)=DHT(J)-HENTH(J,I)
	ELSE
      DHT(J)=ENTHP(J,'L   ',T(I)+1.,PWK)
      HENTH(J,I)=ENTHP(J,'L   ',T(I),PWK)
      DHDT(J,I)=DHT(J)-HENTH(J,I)
	ENDIF
C***  C, ASH
